export type LevelSpellSlotRow = Record<number, number>


export function getSpellSlotTableByClassName(className: string) {
    switch (className) {
        case 'cleric':
        case 'witch':
        case 'druid':
        case 'bard':
        case 'wizard':
            return WIZARD_SPELL_SLOT_TABLE
        case 'oracle':
        case 'sorcerer':
            return SORCERER_SPELL_SLOT_TABLE
        case 'summoner':
        case 'magus':
            return MAGUS_SPELL_SLOT_TABLE
        case 'psychic':
            return PSYCHIC_SPELL_SLOT_TABLE
        default:
            return EMPTY_SPELL_SLOT_TABLE
    }
    return {}
}

